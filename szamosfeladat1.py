szam1:int = int(input("Adj meg egy számot: "))
szam2:int = int(input("Adj meg egy másik számot: "))

osszeadas = szam1 + szam2
szorzas = szam1 * szam2
kulonbseg = szam1 - szam2
hanyados = szam1 / szam2

print(f"A két szám hányaodsa: {hanyados}")
print(f"A két szám különbsége: {kulonbseg}")
print(f"A két szám összege: {osszeadas}")
print(f"A két szám szorzata: {szorzas}")